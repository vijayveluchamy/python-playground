name = 'Vijay Veluchamy'

#Find length
print('Length of the name:', len(name))

#Upper and lower case
print('**Print upper and lowercase**')
print(name.upper())
print(name.lower())


print('------------------')
print('**Find the count of occurrences of a character**')
print(name.count('V'))

print('------------------')
print('**Find the index of a character/string**')
print(name.index('a'))
print(name.index('jay'))

